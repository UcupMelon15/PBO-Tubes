    private void tampilData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tabelPembeli.getModel();
        model.setRowCount(0);
        
        // Header disesuaikan (tanpa password)
        model.setColumnIdentifiers(new Object[]{"ID", "Nama Lengkap", "Nomor HP", "Alamat", "Username"});

        try {
            java.sql.Connection conn = Koneksi.getConnection();
            String sql = "SELECT * FROM pembeli";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_pembeli"),
                    rs.getString("nama_lengkap"),
                    rs.getString("nomor_telepon"),
                    rs.getString("alamat"),
                    rs.getString("username")
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }
