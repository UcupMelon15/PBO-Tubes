    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if (txtNama.getText().isEmpty() || txtUsername.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nama dan Username wajib diisi!");
            return;
        }

        try {
            Pembeli_DAO dao = new Pembeli_DAO();
            dao.tambahPembeli(txtNama.getText(), txtAlamat.getText(), txtNoHp.getText(), txtUsername.getText());
            
            javax.swing.JOptionPane.showMessageDialog(this, "Data pembeli ditambahkan!");
            tampilData();
            
            // Pengosongan Form Inline
            txtNama.setText("");
            txtNoHp.setText("");
            txtAlamat.setText("");
            txtUsername.setText("");
            tabelPembeli.clearSelection();
            idPembeliTerpilih = -1;

        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menambahkan data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (idPembeliTerpilih == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data pembeli pada tabel!");
            return;
        }

        try {
            Pembeli_DAO dao = new Pembeli_DAO();
            dao.updatePembeli(idPembeliTerpilih, txtNama.getText(), txtAlamat.getText(), txtNoHp.getText(), txtUsername.getText());
            
            javax.swing.JOptionPane.showMessageDialog(this, "Data diperbarui!");
            tampilData();
            
            txtNama.setText("");
            txtNoHp.setText("");
            txtAlamat.setText("");
            txtUsername.setText("");
            tabelPembeli.clearSelection();
            idPembeliTerpilih = -1;

        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memperbarui: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    if (idPembeliTerpilih == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus pada tabel!");
            return;
        }
        
        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin hapus pembeli ini?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            try {
                Pembeli_DAO dao = new Pembeli_DAO();
                dao.hapusPembeli(idPembeliTerpilih);
                
                javax.swing.JOptionPane.showMessageDialog(this, "Data dihapus!");
                tampilData();
                
                txtNama.setText("");
                txtNoHp.setText("");
                txtAlamat.setText("");
                txtUsername.setText("");
                tabelPembeli.clearSelection();
                idPembeliTerpilih = -1;
            } catch (java.sql.SQLException e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error Database: " + e.getMessage());
            }
        }    
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tabelPembeliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPembeliMouseClicked
        int baris = tabelPembeli.getSelectedRow();
        if (baris != -1) {
            idPembeliTerpilih = Integer.parseInt(tabelPembeli.getValueAt(baris, 0).toString());
            txtNama.setText(tabelPembeli.getValueAt(baris, 1).toString());
            txtNoHp.setText(tabelPembeli.getValueAt(baris, 2).toString());
            txtAlamat.setText(tabelPembeli.getValueAt(baris, 3).toString());
            txtUsername.setText(tabelPembeli.getValueAt(baris, 4).toString());
        }        
    }//GEN-LAST:event_tabelPembeliMouseClicked

